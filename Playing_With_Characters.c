#include <stdio.h>
int main() 
{
    char a[100];
    char b[100];
    char c[100];
    scanf("%[^\n]%*c", &a);
    scanf("%[^\n]%*c", &b);
    scanf("%[^\n]%*c", &c);
    printf("%s\n%s\n%s",a,b,c);     
    return 0;
}
